from langchain_openai import ChatOpenAI, OpenAIEmbeddings
from langchain.text_splitter import RecursiveCharacterTextSplitter
from langchain.prompts import ChatPromptTemplate
from langchain.chains import RetrievalQA
from qdrant_client import QdrantClient
from qdrant_client.models import Distance, VectorParams, PointStruct
from app.core.config import settings
from typing import List, Dict, Optional
import uuid
import logging

logger = logging.getLogger(__name__)

class RAGService:
    def __init__(self):
        self.embeddings = OpenAIEmbeddings(
            model=settings.OPENAI_EMBEDDING_MODEL,
            openai_api_key=settings.OPENAI_API_KEY
        )
        self.llm = ChatOpenAI(
            model=settings.OPENAI_MODEL,
            openai_api_key=settings.OPENAI_API_KEY,
            temperature=0.1
        )
        self.qdrant = QdrantClient(url=settings.QDRANT_URL)
        self.collection_name = settings.QDRANT_COLLECTION
        self.text_splitter = RecursiveCharacterTextSplitter(
            chunk_size=1000,
            chunk_overlap=200,
            separators=["\n\n", "\n", ".", "?", " "]
        )

    async def init_collection(self):
        collections = self.qdrant.get_collections().collections
        exists = any(c.name == self.collection_name for c in collections)
        if not exists:
            self.qdrant.create_collection(
                collection_name=self.collection_name,
                vectors_config=VectorParams(size=3072, distance=Distance.COSINE)
            )
            logger.info(f"Created Qdrant collection: {self.collection_name}")

    async def ingest_document(self, doc_id: str, title: str, content: str, metadata: Dict = None):
        chunks = self.text_splitter.split_text(content)
        points = []
        for i, chunk in enumerate(chunks):
            embedding = self.embeddings.embed_query(chunk)
            point_id = str(uuid.uuid4())
            payload = {
                "doc_id": doc_id,
                "title": title,
                "chunk_index": i,
                "text": chunk,
                **(metadata or {})
            }
            points.append(PointStruct(id=point_id, vector=embedding, payload=payload))

        if points:
            self.qdrant.upsert(collection_name=self.collection_name, points=points)
        return len(points)

    async def query(self, question: str, top_k: int = 5, filter_metadata: Dict = None) -> Dict:
        query_embedding = self.embeddings.embed_query(question)

        search_params = {"collection_name": self.collection_name, "query_vector": query_embedding, "limit": top_k}

        results = self.qdrant.search(**search_params)

        context_chunks = []
        sources = []
        for result in results:
            context_chunks.append(result.payload["text"])
            sources.append({
                "doc_id": result.payload["doc_id"],
                "title": result.payload["title"],
                "chunk_index": result.payload["chunk_index"],
                "score": result.score
            })

        context = "\n\n---\n\n".join(context_chunks)

        prompt = ChatPromptTemplate.from_messages([
            ("system", """??? ????? ?????? ???? ?????. ??? ??? ??????? ????????? ????? ??? ?????? ?????? ???.
??? ?? ??? ??????? ?? ??????? ?? ??? ?????.
?????? ?????? ??????? ????????? ???????? ??? ?????.
??? ?????? ???????."""),
            ("human", """?????? ????????:
{context}

??????: {question}

??? ???? ???? ?? ????????? ???????? ?????????:""")
        ])

        chain = prompt | self.llm
        response = await chain.ainvoke({"context": context, "question": question})

        return {
            "answer": response.content,
            "sources": sources,
            "chunks_used": len(context_chunks)
        }

